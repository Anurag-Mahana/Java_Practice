package Session_2k26.a1_Flowcharts;
import java.util.*;
public class a1_Sum_of_two_nos {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter value for num a:");
        int a = sc.nextInt();
        System.out.println("Enter value for num b:");
        int b = sc.nextInt();
        int sum=a+b;
        System.out.println("Sum of "+a+"+"+b+" = "+sum);
    }
}
